package cn.data58.demo58;

public class User66 {
    private String name;
    private String sex;
    private String hobby;

    public User66() {
        super();
    }

    public User66(String name, String sex, String hobby) {
        super();
        this.name = name;
        this.sex = sex;
        this.hobby = hobby;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    public String getHobby() {
        return hobby;
    }

    public void setHobby(String hobby) {
        this.hobby = hobby;
    }
}
